function add(a, b){
  var result = a + b;
  return result;
}
function subtract(a, b){
  var result = a - b;
  return result;
}
function multiply(a, b){
  var result = a * b;
  return result;
}
function divide(a, b){
  var result = a / b;
  return result;
}
function inc(n){
  var result = ++n;
  return result;
}
function dec(n){
  var result = n--;
  return result;
}